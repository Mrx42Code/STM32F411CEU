//-Public----------------------------------------------------------------------
// Name: MemoryLoadfromFlash(const char* FileName[] ,uint16_t FileItems ,uint16_t address ,uint16_t MemSize)
//-----------------------------------------------------------------------------
void MC_FileSystem6502::MemoryLoadfromFlash(const char* FileName[] ,uint16_t FileItems ,uint16_t address ,uint16_t MemSize)
{
	uint16_t i;
	uint16_t l;
	uint16_t LineCount = FileItems;
	uint16_t LineLen;
	uint16_t EndOfDataLine;
	std::string StringLine;
	std::string StringLineStart;
	std::string StringNumberOfBytes;
	std::string StringMemAddress;
	std::string StringRecordType;
	std::string StringData;
	std::string StringByte;
	std::string StringCrc;
	std::string StringCrcCal;
	uint8_t NumberOfBytes;
	uint16_t MemAddress;
	uint8_t RecordType;
	uint8_t Data;
	uint8_t Crc;
	uint8_t LineCrc;
	uint16_t RamAddress;

	for (i = 0; i < LineCount; i++) {
		StringLine = FileName[i];
		if(StringLine.length() >= 11) {
			LineLen = strlen(StringLine.c_str());
			StringCrc = StringLine.substr(LineLen - 2, 2);
			StringCrcCal = StringLine.substr(1, LineLen - 3);
			LineCrc = 0;
			for (l = 0; l < StringCrcCal.length(); l+=2) {
				StringByte = StringCrcCal.substr(l, 2);
				Data = Hex2Dec(StringByte);
				LineCrc += Data;
			}
			LineCrc ^= 0xFF;
			LineCrc ++;
			Crc = Hex2Dec(StringCrc);
			if(LineCrc == Crc) {
				StringLineStart = StringLine.substr(0, 1);
				StringNumberOfBytes = StringLine.substr(1, 2);
				StringMemAddress = StringLine.substr(3, 4);
				StringRecordType = StringLine.substr(7, 2);
				EndOfDataLine = LineLen - (8 + 2);
				StringData = StringLine.substr(9, EndOfDataLine);
				if(StringData.length()) {
					NumberOfBytes = Hex2Dec(StringNumberOfBytes);
					MemAddress = Hex2Dec(StringMemAddress);
					RecordType = Hex2Dec(StringRecordType);
					if(NumberOfBytes == 0) {
						return;
					}
					if(RecordType == 0 && NumberOfBytes > 0) {
						for (l = 0; l < StringData.length(); l+=2) {
							StringByte = StringData.substr(l, 2);
							Data = Hex2Dec(StringByte);
							if(MemAddress <= MemSize) {
								RamAddress = address + MemAddress;
								mc_Hardware6502.m_MemoryWriteOverride = true;
								mc_Hardware6502.CpuMemoryMapWrite(RamAddress, Data);
								mc_Hardware6502.m_MemoryWriteOverride = false;
							}
							MemAddress++;
						}
					}
				}
			} else {
				printf("Crc Error %d %d %s\r\n", Crc, LineCrc, StringLine.c_str());
			}
		}
	}
}
//-Public----------------------------------------------------------------------
// Name: Crc32(const uint8_t* buf, uint32_t buflen)
//-----------------------------------------------------------------------------
uint32_t MC_FileSystem6502::Crc32(const uint8_t* buf, uint32_t buflen)
{
	uint32_t crc = (~((uint32_t)0));

	if (!buf)
		return 0;

	while ((buflen & 0xF) != 0) {
		STEPCRC(crc, *buf); buf++;
		buflen--;
	}
	buflen >>= 4; /* 16 at a time */
	while (buflen--) {
		STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;	STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;
		STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;	STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;
		STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;	STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;
		STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;	STEPCRC(crc, *buf); buf++; STEPCRC(crc, *buf); buf++;
	}
	crc ^= 0xFFFFFFFF;
	return crc;
}











